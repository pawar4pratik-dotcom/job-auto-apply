@app.route("/api/bot/start", methods=["POST"])
def api_start_bot():
    data = request.json
    platform = data.get("platform", "all")
    company = data.get("company", "")
    headless = data.get("headless", False)

    bot_key = platform
    if running_bots.get(bot_key):
        return jsonify({"success": False, "message": f"{platform} bot is already running!"})

    if platform == "all":
        cmd = [sys.executable, "main.py"]
    else:
        cmd = [sys.executable, "main.py", f"--{platform}"]

    if headless:
        cmd.append("--headless")
    if company:
        cmd += ["--company", company]

    thread = threading.Thread(target=stream_bot, args=(bot_key, cmd), daemon=True)
    thread.start()
    return jsonify({"success": True, "message": f"{platform.title()} bot started!", "key": bot_key})


