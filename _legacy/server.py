def get_applications():
    tracker_path = os.path.join(os.getcwd(), "logs", "job_applications.csv")
    if not os.path.exists(tracker_path):
        return []
    rows = []
    try:
        with open(tracker_path, "r", encoding="utf-8") as f:
            reader = csv.DictReader(f)
            for row in reader:
                rows.append(dict(row))
    except Exception:
        pass
    return list(reversed(rows))


def get_stats():
    apps = get_applications()
    total = len(apps)
    applied = sum(1 for a in apps if a.get("Status") == "Applied")
    skipped = sum(1 for a in apps if a.get("Status") == "Skipped")
    manual = sum(1 for a in apps if a.get("Status") == "Manual Needed")
    today = datetime.date.today().strftime("%Y-%m-%d")
    today_count = sum(1 for a in apps if a.get("Date", "").startswith(today))
    portals = {}
    for a in apps:
        p = a.get("Portal", "Other")
        portals[p] = portals.get(p, 0) + 1
    return {
        "total": total, "applied": applied, "skipped": skipped,
        "manual": manual, "today": today_count, "portals": portals,
    }


def stream_bot(bot_key, cmd):
    bot_logs[bot_key] = []
    running_bots[bot_key] = True
    try:
        proc = subprocess.Popen(
            cmd, stdout=subprocess.PIPE, stderr=subprocess.STDOUT,
            text=True, encoding="utf-8", errors="replace", cwd=os.getcwd()
        )
        for line in proc.stdout:
            bot_logs[bot_key].append(line.rstrip())
        proc.wait()
    except Exception as e:
        bot_logs[bot_key].append(f"[ERROR] {e}")
    finally:
        running_bots[bot_key] = False


